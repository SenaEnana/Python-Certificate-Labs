class Rectangle:
    """A class representing a rectangle shape."""

    def __init__(self, width: int, height: int) -> None:
        self.width = width
        self.height = height
    def set_width(self, width: int) -> None:
        self.width = width
    def set_height(self, height: int) -> None:
        self.height = height
    def get_area(self) -> int:
        return self.width * self.height





















